import { toast } from "react-toastify";

export const toastSuccess = (channel:string) => {
    toast(`${channel} channel successfully created`, {
        position: "top-right",
        autoClose: 3000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "light",
      });
}

